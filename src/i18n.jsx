const I18nContext = createContext(null);

const getInitialLanguage = () => {
  if (typeof window === "undefined") return DEFAULT_LANGUAGE;
  const saved = window.localStorage.getItem(STORAGE_KEY);
  return saved === "ar" || saved === "en" ? saved : DEFAULT_LANGUAGE;
};

const interpolate = (value, params = {}) =>
  Object.entries(params).reduce(
    (text, [key, replacement]) => text.replaceAll(`{{${key}}}`, String(replacement)),
    value,
  );

export const I18nProvider = ({ children }) => {
  const [language, setLanguage] = useState(getInitialLanguage);
  const direction = language === "ar" ? "rtl" : "ltr";

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.documentElement.dir = direction;
  }, [direction, language]);

  const value = useMemo(() => {
    const t = (key, params) => {
      const entry = resources[language]?.[key] ?? resources.en[key] ?? key;
      return typeof entry === "string" ? interpolate(entry, params) : entry;
    };

    const toggleLanguage = () => setLanguage((current) => (current === "en" ? "ar" : "en"));

    return {
      language,
      direction,
      isRtl: direction === "rtl",
      setLanguage,
      toggleLanguage,
      t,
    };
  }, [direction, language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
};
